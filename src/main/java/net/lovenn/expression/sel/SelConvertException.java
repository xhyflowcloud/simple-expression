package net.lovenn.expression.sel;

public class SelConvertException extends RuntimeException {

    public SelConvertException() {
    }

    public SelConvertException(String message) {
        super(message);
    }

    public SelConvertException(String message, Throwable cause) {
        super(message, cause);
    }

    public SelConvertException(Throwable cause) {
        super(cause);
    }
}
